# 資料: 程式的基本單位
# 數字
3456
3.5
# 字串
"測試中文"
# 布林值
False
True
# 順序、可動的列表 List
[3, 4, 5]
["hello", "world"]
# 有順序，不可動的列表 Tuple
(3, 4, 5)
("hello", "world")
# 集合 Set
{3, 4, 5}
{"hello", "world"}
# 字典 Dictionary
{"apple": "fruit", "data":"information"}
{"apple": "蘋果", "data":"資料"}
# 變數: 用來儲存資料的
x = [1, 2, 3]
print(x)
x = {3, 4, 5}
print(x)